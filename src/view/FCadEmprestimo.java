    public void carregarCampos(Emprestimo e) throws ParseException {
        SimpleDateFormat in = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat out = new SimpleDateFormat("dd/MM/yyyy");
        String data = out.format(in.parse(e.getData().toString()));
        edData.setText(data);
        ccAluno.setSelectedItem((Emprestimo) e);
        ccLivro.setSelectedItem((Emprestimo) e);
    }

