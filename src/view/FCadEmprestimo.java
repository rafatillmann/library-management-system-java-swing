    private void preencherAlunos() {
        for (Aluno a : controlAluno.getAlunos()) {
            ccAluno.addItem(a);
        }
    }

    private void preencherLivros() {
        for (Livro l : controlLivro.getLivros()) {
            ccLivro.addItem(l);
        }
    }

